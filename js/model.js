class Country{
    constructor(flags, name, population, region, capital, native_name, sub_region, top_level_domain, currencies, lenguages, border_countries){
        this.flags = flags;
        this.name = name;
        this.population = population;
        this.region = region;
        this.capital = capital;
        this.native_name = native_name;
        this.sub_region = sub_region;
        this.top_level_domain = top_level_domain;
        this.currencies = currencies;
        this.lenguages = lenguages;
        this.border_countries = border_countries;
    }
}